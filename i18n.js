function getCurrentLang() {
  return localStorage.getItem("lang") || "en";
}

// ===============================
// ğŸŒ å¥—ç”¨èªè¨€åˆ°æ‰€æœ‰ data-i18n
// ===============================
function applyLanguage(lang) {
  const dict = i18nText[lang] || i18nText.en;

  document.documentElement.lang = lang === "zh" ? "zh-Hant" : "en";

  // æ›´æ–°æ‰€æœ‰ data-i18n å…ƒç´ 
  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.dataset.i18n;
    if (dict[key]) {
      el.textContent = dict[key];
    }
  });

  // å„²å­˜ä½¿ç”¨è€…èªè¨€åå¥½
  localStorage.setItem("lang", lang);

  // é€šçŸ¥å…¶ä»– JSï¼ˆä¾‹å¦‚ index.jsï¼‰èªè¨€å·²è®Šæ›´
  document.dispatchEvent(new CustomEvent("languageChange", { detail: { lang } }));
}

// ===============================
// ğŸŒ åˆå§‹åŒ–èªè¨€ + ç¶å®šæŒ‰éˆ•
// ===============================
document.addEventListener("DOMContentLoaded", () => {
  const savedLang = getCurrentLang();
  applyLanguage(savedLang);

  document.querySelectorAll("[data-lang]").forEach((btn) => {
    btn.addEventListener("click", () => {
      applyLanguage(btn.dataset.lang);
    });
  });

  // åŒæ­¥ <title>
  const titleKey = "site_title";
  if (i18nText[savedLang][titleKey]) {
    document.title = i18nText[savedLang][titleKey];
  }
});
